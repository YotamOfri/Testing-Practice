const calculator = {
  add: (number, number_2) => {
    return number + number_2;
  },
  subtract: (number, number_2) => {
    return number - number_2;
  },
  divide: (number, number_2) => {
    return number / number_2;
  },
  multiply: (number, number_2) => {
    return number * number_2;
  },
};

module.exports = calculator;
